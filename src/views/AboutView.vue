    <h1>What is Lux?</h1>
Lux is a web-based activity to help communities become more aware of their silent identities

 
<h2> How does it work? </h2>
A Lux is created with questions like: ‘Have you been bullied at school?’ or ‘Have you been sexually harassed by someone from our community?’ A link to the Lux is shared out to the members who each privately answer the questions. All answers are stored anonymously. Then at an assembly or meeting, each member goes to the shared link (like Kahoot) and is anonymously given the answers of someone else. The questions are read aloud and people stand up when their given answer is "Yes". Although numbers and graphs can make the same points, this exercise is especially powerful because you see your peers standing and know that they are giving voice to the identity of someone else in the room.

 
<h2> Inspiration for Lux </h2>
The idea and structure of Lux is based off similar activities led by affinity groups at Head-Royce School in Oakland, CA. The inspiration to create a website to run Lux activities came from the #MeToo movement and an increased interest to understand the prevalence of issues like racism and sexual harassment that are too often ignored.


 
